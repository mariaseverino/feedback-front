export const getUserIcon = (iconId: string) => {
    switch (iconId) {
        case 'icon-user-1':
            return 'https://github.com/shadcn.png';
        case 'icon-user-2':
            return 'https://github.com/evilrabbit.png';
        case 'icon-user-3':
            return 'https://github.com/maxleiter.png';

        default:
            'https://github.com/shadcn.png';
    }
};
